package Day_10;

class Employee{

    String emp_name; 
    double salary;

    Employee(String emp_name,double salary){

        emp_name = emp_name;
        salary = salary;

    }

    String get_name(){
        return emp_name;
    }

    double get_salary(){
        return salary;
    }

    void set_name(String emp_name){
        emp_name = emp_name;
    }

    void set_salary(double salary){
        salary = salary;
    }

    


}